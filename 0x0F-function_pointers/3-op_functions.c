#include "3-calc.h"

/**
 * op_add - addition
 * @a: first int
 * @b: second int
 * Return: sum of two int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: first int
 * @b: second int
 * Return: subtract of two int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply
 * @a: first int
 * @b: second int
 * Return: multiply two int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: first int
 * @b: second int
 * Return: division two int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus
 * @a: first int
 * @b: second int
 * Return: remainder division between two int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
